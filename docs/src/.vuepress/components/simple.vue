<script>
import { Container, Draggable } from "../../../../src/main.js";
import { applyDrag, generateItems } from "../utils/helpers";

export default {
  name: "Simple",

  components: { Container, Draggable },

  data() {
    return {
      items: generateItems(10, (i) => ({ id: i, data: "Draggable " + i })),
    };
  },

  methods: {
    onDrop(dropResult) {
      this.items = applyDrag(this.items, dropResult);
    },
    getGhostParent() {
      return document.body;
    },
    onDropReady(dropResult) {
      console.log("drop ready", dropResult);
    },
    dropNotAllowed({ payload, container }) {
      console.log("drop not allowed");
    },
  },
};
</script>
<template>
  <div class="simple-page">
    <Container
      :get-ghost-parent="getGhostParent"
      :remove-on-drop-out="true"
      container-class="class-container"
      tag="article"
      @drop="onDrop"
      @drop-ready="onDropReady"
      @drop-not-allowd="dropNotAllowed"
    >
      <Draggable v-for="item in items" :key="item.id" tag="span">
        <div class="draggable-item">
          {{ item.data }}
        </div>
      </Draggable>
    </Container>
  </div>
</template>
